export {};
const fs = require("fs");

const dataset = fs.readFileSync(
  "advent-of-code/2025/day-7/data.txt",
  "utf8",
  () => null
);

const data = dataset.split("\n").map((line) => line.split(""));
const splitters = new Set();
let start = "";

data.forEach((line, i) => {
  line.forEach((item, j) => {
    if (item === "^") splitters.add(`${i};${j}`);
    if (item === "S") start = `${i};${j}`;
  });
});

const splits = new Set();
const rays = new Map();

rays.set(start, 1);

const findSplits = () => {
  let lastLineCounter = 0;

  data.forEach((line, i) => {
    line.forEach((item, j) => {
      const currIdx = `${i};${j}`;
      if (rays.has(currIdx)) {
        const downwardIdx = `${i + 1};${j}`;

        if (!splitters.has(downwardIdx)) {
          const curr = rays.get(currIdx);
          if (rays.has(downwardIdx)) {
            rays.set(downwardIdx, rays.get(downwardIdx) + curr);
          } else {
            rays.set(downwardIdx, curr);
          }
        } else {
          splits.add(downwardIdx);
          const curr = rays.get(currIdx);

          const leftIdx = `${i + 1};${j - 1}`;
          const rightIdx = `${i + 1};${j + 1}`;

          if (rays.has(leftIdx)) {
            rays.set(leftIdx, rays.get(leftIdx) + curr);
          } else rays.set(leftIdx, curr);

          if (rays.has(rightIdx)) {
            rays.set(rightIdx, rays.get(rightIdx) + curr);
          } else rays.set(rightIdx, curr);
        }
      }
      if (i === data.length - 1) {
        if (rays.get(currIdx)) lastLineCounter += rays.get(currIdx);
      }
    });
  });
  return lastLineCounter;
};
console.log(findSplits());
